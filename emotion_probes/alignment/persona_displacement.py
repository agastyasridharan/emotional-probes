def _apply_suite_env(key: str) -> None:
    """Pin Config's env vars from the suite manifest (same lines run_suite.sh evals)."""
    from emotion_probes.models import suite as suite_mod

    for line in suite_mod.env_exports(suite_mod.by_key(key)):
        k, v = line.removeprefix("export ").split("=", 1)
        os.environ[k] = v


def main() -> None:
    parser = argparse.ArgumentParser(
        description="E3 persona-coordinate displacement: re-encode the existing "
                    "scored free-form generations, capture layers {32,42,50}, and "
                    "project onto the frozen E1 persona coordinates."
    )
    parser.add_argument("--suite", default=None,
                        help="suite key (e.g. qwen3-32b) — exports the manifest env "
                             "before Config() when the runner has not already done so")
    parser.add_argument("--scored", default=None,
                        help="path to qualia_freeform_scored.json "
                             "(default: <analysis_dir>/qualia_freeform_scored.json)")
    parser.add_argument("--extra-scored", nargs="+", default=[],
                        help="additional scored files to merge (e.g. the "
                             "__random__/__mean_all__ top-up run)")
    parser.add_argument("--layers", nargs="+", type=int, default=DEFAULT_CAPTURE_LAYERS,
                        help=f"capture layers (default: {DEFAULT_CAPTURE_LAYERS})")
    parser.add_argument("--arms", nargs="+", default=None, choices=ALL_ARMS,
                        help="restrict to these arms (default: all)")
    parser.add_argument("--emotions", nargs="+", default=None,
                        help="restrict to these condition names (baselines always kept)")
    parser.add_argument("--strengths", nargs="+", type=float, default=None,
                        help=f"confirmatory strengths (default: {list(CONFIRMATORY_STRENGTHS)}; "
                             "s=0.15 records are always kept, flagged descriptive_only)")
    parser.add_argument("--directions", default=None,
                        help="npz of frozen directions (keys '<name>@L<layer>') — "
                             "fallback when persona.geometry is not importable")
    parser.add_argument("--n-control", type=int, default=DEFAULT_CONTROL_N,
                        help="generations per (role, frame) in the persona control")
    parser.add_argument("--max-new-tokens", type=int, default=DEFAULT_MAX_NEW_TOKENS)
    parser.add_argument("--temperature", type=float, default=DEFAULT_TEMPERATURE)
    parser.add_argument("--gen-batch", type=int, default=DEFAULT_GEN_BATCH)
    parser.add_argument("--quick", action="store_true",
                        help="smoke subset (few conditions, 2 records/cell, tiny control)")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--tag", default="",
                        help="suffix for the output filename (never clobbers the main run)")
    args = parser.parse_args()

    emotions, n_control, max_per_cell = args.emotions, args.n_control, None
    if args.quick:
        emotions = emotions or QUICK_EMOTIONS
        n_control = QUICK_CONTROL_N if args.n_control == DEFAULT_CONTROL_N else args.n_control
        max_per_cell = QUICK_RECORDS_PER_CELL

    if args.suite:
        _apply_suite_env(args.suite)
    config = Config()
    model = ProbedModel(config)
    bank = ProbeBank.load(config.emotion_vectors_path)
    bank.check_against(model, where="persona_displacement")
    experiment = PersonaDisplacementExperiment(model, bank, config,
                                               capture_layers=args.layers)
    directions = (load_directions_npz(args.directions, args.layers)
                  if args.directions else None)
    payload = experiment.run(
        scored_path=args.scored, extra_scored=args.extra_scored, arms=args.arms,
        emotions=emotions, strengths=args.strengths, directions=directions,
        n_control=n_control, max_records_per_cell=max_per_cell,
        max_new_tokens=args.max_new_tokens, temperature=args.temperature,
        gen_batch=args.gen_batch, seed=args.seed)
    path = experiment.save_json(f"persona_displacement{args.tag}.json", payload)

    print(f"wrote {path}")
    print(f"model={payload['model_id']}  suite={payload['suite_key']}  "
          f"steer_layer={payload['steer_layer']}  capture={payload['capture_layers']}  "
          f"calib_norm={payload['calibration_norm']:.2f}")
    by_arm: dict[str, int] = {}
    for r in payload["records"]:
        by_arm[r["arm"]] = by_arm.get(r["arm"], 0) + 1
    print(f"kept_source_records={payload['n_kept_records']}  "
          f"skipped_conditions={payload['skipped_conditions']}")
    for arm in payload["arms"]:
        print(f"  {arm:<26} {by_arm.get(arm, 0):>6} rows")
    # sanity: persona-control assistant-axis displacement vs unsteered baselines
    L = max(payload["capture_layers"])
    base_p = [r["projections"]["assistant_axis"] for r in payload["records"]
              if r["arm"] == ARM_REENC_UNSTEERED and r["condition"] == BASELINE
              and r["layer"] == L]
    ctrl_p = [r["projections"]["assistant_axis"] for r in payload["records"]
              if r["arm"] == ARM_PERSONA_CONTROL and r["layer"] == L]
    if base_p and ctrl_p:
        print(f"  persona-control assistant-axis displacement @L{L}: "
              f"{float(np.mean(ctrl_p) - np.mean(base_p)):+.3f} "
              "(negative = roles drift off-Assistant, as expected)")


if __name__ == "__main__":
    main()
