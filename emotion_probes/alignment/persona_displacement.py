def _snap(s) -> float:
    """Snap a strength for use as a grouping key (compute_qualia_stats convention)."""
    return round(float(s), 6)


def _unit(v: np.ndarray) -> np.ndarray:
    v = np.asarray(v, dtype=np.float32)
    n = float(np.linalg.norm(v))
    if n == 0.0 or not np.isfinite(n):
        raise ValueError(f"cannot unit-normalize direction with norm {n}")
    return v / n


# --------------------------------------------------------------------------- #
# Multi-layer capture — in-driver helper modeled on ProbedModel._capture
# (language_model.py is owned by another workstream and must not be edited).
# --------------------------------------------------------------------------- #
@contextmanager
def multi_layer_capture(model: ProbedModel, layers: Sequence[int]) -> Iterator[dict]:
    """Forward hooks on several decoder layers at once; yields ``{layer: tensor}``
    repopulated on every forward pass while open (each tensor ``(B, T, H)``,
    detached). Same mechanism as :meth:`ProbedModel._capture`, but private to
    this driver so E3 never depends on wrapper internals beyond ``_layers``."""
    captured: dict[int, object] = {}
    handles = []

    def make_hook(idx: int):
        def hook(_module, _inp, output):
            captured[idx] = (output[0] if isinstance(output, tuple) else output).detach()
        return hook

    for idx in layers:
        handles.append(model._layers[idx].register_forward_hook(make_hook(idx)))
    try:
        yield captured
    finally:
        for h in handles:
            h.remove()


