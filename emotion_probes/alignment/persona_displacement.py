# Frozen E1 coordinates
# --------------------------------------------------------------------------- #
def experiencer_axis(ax, subsets: dict, layer: int) -> np.ndarray:
    """z_x at one layer, exactly as pre-registered (prereg E3 "Coordinates"):
    unit(mean frozen experiencer roles − matched non-experiencer roles) from the
    275-role atlas, **orthogonalized against a**. The role sets are the FROZEN
    adjudicated ``experiencer_high``/``experiencer_low`` subsets
    (docs/prereg/role_subsets.json) — never re-derived from ratings here."""
    sub = subsets.get("subsets", subsets)
    try:
        high, low = list(sub["experiencer_high"]), list(sub["experiencer_low"])
    except (KeyError, TypeError) as exc:
        keys = sorted(sub) if isinstance(sub, dict) else type(sub).__name__
        raise KeyError(
            "role_subsets has no frozen 'experiencer_high'/'experiencer_low' sets "
            f"(found: {keys}) — E3's z_x coordinate requires the frozen "
            "experiencer role sets (portfolio Day-0 item 4)") from exc
    missing = [n for n in [*high, *low] if n not in ax.role_vectors]
    if missing:
        raise KeyError(f"experiencer roles missing from the role atlas: {missing}")
    x = (np.mean([ax.role_vectors[n][layer] for n in high], axis=0)
         - np.mean([ax.role_vectors[n][layer] for n in low], axis=0)).astype(np.float32)
    a = ax.unit_axis(layer)
    x = x - float(x @ a) * a                    # prereg: orthogonalized against a
    return _unit(x)


def frozen_directions(layers: Sequence[int], k_pcs: int = 8, seed: int = 0) -> dict[int, dict]:
    """Build ``{layer: {name: unit vector}}`` from the frozen persona assets +
    E1 geometry (see INTEGRATION in the module docstring). This is the ONLY
    place that touches ``emotion_probes.persona.geometry``."""
    from emotion_probes.persona import assets
    from emotion_probes.persona import geometry  # E1 workstream

    ax = assets.load_assistant_axis()
    assets.anchor_check(ax)                       # sign/index convention guard
    subsets = assets.load_role_subsets()
    ebank = assets.load_emotion_bank()
    out: dict[int, dict] = {}
    for layer in layers:
        g = geometry.build_directions(ebank, ax, subsets, int(layer), seed=seed)
        d = {
            "assistant_axis": _unit(g["a"]),
            "human_axis": _unit(g["h"]),
            "human_axis_purged": _unit(g["h_purged"]),
            "experiencer_axis": experiencer_axis(ax, subsets, int(layer)),
            "valence_axis": _unit(g["valence"]),
        }
        atlas = geometry.pca_atlas(ax, int(layer), center="default", n_pcs=k_pcs)
        for i in range(int(atlas["n_pcs"])):
            d[f"pc{i + 1}"] = _unit(np.asarray(atlas["components"])[i])
        out[int(layer)] = d
    return out


def load_directions_npz(path: str | Path, layers: Sequence[int]) -> dict[int, dict]:
    """Load pre-exported frozen directions from an ``.npz`` with keys
    ``"<name>@L<layer>"`` (the E1 export fallback)."""
    z = np.load(Path(path))
    out: dict[int, dict] = {}
    for layer in layers:
        d = {}
        for key in z.files:
            if key.endswith(f"@L{int(layer)}"):
                d[key.rsplit("@L", 1)[0]] = _unit(z[key])
        if not d:
            raise KeyError(f"{path}: no '<name>@L{int(layer)}' keys for layer {layer}")
        out[int(layer)] = d
    return out


def load_role_instruction(role: str, root: str | Path | None = None) -> str:
    """First 'pos' system-prompt variant for a role from the assistant-axis repo
    data (``roles/instructions/<role>.json``)."""
    root = Path(root) if root is not None else DEFAULT_ROLE_ROOT
    with open(root / f"{role}.json") as fh:
        spec = json.load(fh)
    return spec["instruction"][0]["pos"]


# --------------------------------------------------------------------------- #
# The experiment
# --------------------------------------------------------------------------- #
class PersonaDisplacementExperiment(QualiaSteeringExperiment):
    """Forward-only persona-coordinate displacement decomposition (E3).

    Inherits the single ~2/3-depth steering layer, ``_synthetic_vectors`` and the
    ``_steer`` routing (real emotions -> engine, synthetic -> ``model.steer`` at
    the matched calibrated norm) from the logit driver; adds simultaneous
    multi-layer capture, teacher-forced re-encoding, the analytic direct-add
    null, and the system-prompt persona positive control.
    """

    def __init__(self, model: ProbedModel, bank: ProbeBank, config: Config | None = None,
                 capture_layers: Sequence[int] = DEFAULT_CAPTURE_LAYERS):
        super().__init__(model, bank, config)     # sets self.layer (steering layer)
        self.capture_layers = sorted(int(l) for l in capture_layers)

    # ------------------------------------------------------------------ #
    # Rendering + forwards
    # ------------------------------------------------------------------ #
    def _render_conv(self, msgs: list[dict]) -> str:
        """Render a message list through the chat template (no YES/NO suffix)."""
        kwargs = {}
        if self.config.enable_thinking is not None:
            kwargs["enable_thinking"] = self.config.enable_thinking
        return self.model.tokenizer.apply_chat_template(
            msgs, tokenize=False, add_generation_prompt=True, **kwargs
        )

    def _forward_tokens(self, token_ids: Sequence[int]) -> dict[int, np.ndarray]:
        """One teacher-forced forward pass; residual stream at every capture
        layer, ``{layer: (T, H) float32}``."""
        import torch

        input_ids = torch.tensor([list(token_ids)], device=self.model._input_device)
        with multi_layer_capture(self.model, self.capture_layers) as cap:
            with torch.no_grad():
                self.model.model(input_ids=input_ids)
        return {L: cap[L].to(torch.float32).cpu().numpy()[0] for L in self.capture_layers}

    def _reencode(self, rendered_prompt: str, response: str,
                  n_prompt: int | None = None, max_length: int | None = None) -> dict:
        """Teacher-force ``[prompt + response]``, mean-pool the response span.

        The span starts at ``n_prompt`` (the RECORDED ``prompt_tokens`` when
        available — prereg E3 — else the tokenized prompt length), clamped so at
        least one token is pooled (same convention as
        :meth:`ProbedModel.mean_residual_over_span`). Also pools the washout-
        guard positions (first-person-pronoun / experience-claim tokens)."""
        tok = self.model.tokenizer
        max_length = max_length or getattr(self.config, "deflection_max_length", None) or 4096
        full_ids = tok(rendered_prompt + response, add_special_tokens=False)["input_ids"]
        full_ids = full_ids[:max_length]
        if n_prompt is None:
            n_prompt = len(tok(rendered_prompt, add_special_tokens=False)["input_ids"])
        n_prompt = min(int(n_prompt), max(0, len(full_ids) - 1))

        acts = self._forward_tokens(full_ids)
        resp_ids = full_ids[n_prompt:]
        washout_idx = [i for i, tid in enumerate(resp_ids)
                       if tok.decode([tid]).strip().lower() in WASHOUT_TOKENS]

        pooled, washout_pooled = {}, {}
        for L, a in acts.items():
            span = a[n_prompt:]
            if span.shape[0] == 0:
                span = a[-1:]
            pooled[L] = span.mean(axis=0)
            washout_pooled[L] = (span[washout_idx].mean(axis=0) if washout_idx else None)
        return {"pooled": pooled, "washout_pooled": washout_pooled,
                "washout_tokens": len(washout_idx), "response_tokens": len(resp_ids)}

    def _prompt_acts(self, rendered_prompt: str) -> dict[int, np.ndarray]:
        """Residual stream at the FINAL prompt position (the pre-response
        position where generation would begin), ``{layer: (H,)}``."""
        ids = self.model.tokenizer(rendered_prompt, add_special_tokens=False)["input_ids"]
        acts = self._forward_tokens(ids)
        return {L: a[-1] for L, a in acts.items()}

    def _gen_samples(self, rendered_prompt: str, n: int, max_new_tokens: int,
                     temperature: float, gen_batch: int) -> list[str]:
        """n sampled continuations for one rendered prompt (chunked batched gen)."""
        outs: list[str] = []
        for start in range(0, n, gen_batch):
            k = min(gen_batch, n - start)
            outs.extend(self.model.generate_batch(
                [rendered_prompt] * k, max_new_tokens=max_new_tokens,
                temperature=temperature, do_sample=True, chat=False,
            ))
        return outs

    # ------------------------------------------------------------------ #
    # Projections
    # ------------------------------------------------------------------ #
    @staticmethod
    def _project(vec: np.ndarray, dirs: dict, v_e_layer: np.ndarray | None,
                 v_e_steer: np.ndarray | None) -> dict:
        """Raw (uncentered) scalar projections of one pooled activation onto the
        frozen unit coordinates + the injected direction (layer-local and
        as-injected). Uncentered by design: every statistic downstream is a
        within-frame difference against the strength-0 baselines, which cancels
        any constant offset exactly."""
        vec = np.asarray(vec, dtype=np.float32)
        out = {name: round(float(vec @ d), 5) for name, d in dirs.items()}
        out["v_injected"] = (round(float(vec @ v_e_layer), 5)
                             if v_e_layer is not None else None)
        out["v_injected_steer"] = (round(float(vec @ v_e_steer), 5)
                                   if v_e_steer is not None else None)
        return out

    def _emit(self, rows: list[dict], base: dict, dirs: dict,
              pooled: dict[int, np.ndarray], v_e_layer: dict | None,
              v_e_steer: np.ndarray | None, washout_pooled: dict | None = None,
              washout_tokens: int | None = None) -> None:
        """Append one flat output record per capture layer."""
        for L in self.capture_layers:
            row = dict(base)
            row["layer"] = int(L)
            row["projections"] = self._project(
                pooled[L], dirs[L],
                v_e_layer.get(L) if v_e_layer is not None else None, v_e_steer)
            if washout_pooled is not None:
                w = washout_pooled.get(L)
                row["projections_washout"] = (
                    self._project(w, dirs[L],
                                  v_e_layer.get(L) if v_e_layer is not None else None,
                                  v_e_steer)
                    if w is not None else None)
                row["washout_tokens"] = int(washout_tokens or 0)
            rows.append(row)

    # ------------------------------------------------------------------ #
    # The sweep
    # ------------------------------------------------------------------ #
    def run(self, scored_path: str | Path | None = None,
            extra_scored: Sequence[str | Path] = (),
            arms: Sequence[str] | None = None,
            emotions: Sequence[str] | None = None,
            strengths: Sequence[float] | None = None,
            directions: dict[int, dict] | None = None,
            n_control: int = DEFAULT_CONTROL_N,
            control_roles: Sequence[str] = CONTROL_ROLES,
            role_root: str | Path | None = None,
            max_records_per_cell: int | None = None,
            max_new_tokens: int = DEFAULT_MAX_NEW_TOKENS,
            temperature: float = DEFAULT_TEMPERATURE,
            gen_batch: int = DEFAULT_GEN_BATCH, seed: int = 0) -> dict:
        """Re-encode the scored generations through every requested arm.

        Forward-only except ``persona_control`` (the one arm that generates).
        One steer context per (condition, strength) cell; unknown conditions
        (e.g. extra random draws whose seeds this driver cannot reconstruct) are
        excluded from steered/analytic arms and listed in the header."""
        import torch

        np.random.seed(seed)
        torch.manual_seed(seed)

        arms = list(arms) if arms is not None else list(ALL_ARMS)
        bad = [a for a in arms if a not in ALL_ARMS]
        if bad:
            raise ValueError(f"unknown arms {bad}; valid: {ALL_ARMS}")

        # ---- sources ----
        scored_path = (Path(scored_path) if scored_path is not None
                       else self.config.analysis_dir / "qualia_freeform_scored.json")
        sources = [("main", Path(scored_path))]
        sources += [(f"extra{i}", Path(p)) for i, p in enumerate(extra_scored)]
        all_records, frames_meta, source_meta = load_scored(sources)
        confirmatory = list(strengths) if strengths is not None else list(CONFIRMATORY_STRENGTHS)
        kept = select_records(all_records, confirmatory, DESCRIPTIVE_STRENGTHS,
                              emotions=emotions, max_per_cell=max_records_per_cell)
        kept = [r for r in kept if r["frame"] in frames_meta]

        # frames: rendered prompt + recorded prompt_tokens (fallback: re-render)
        for key, f in frames_meta.items():
            if not f.get("rendered"):
                f["rendered"] = self._render_conv([{"role": "user", "content": f["text"]}])
            if not f.get("prompt_tokens"):
                f["prompt_tokens"] = len(self.model.tokenizer(
                    f["rendered"], add_special_tokens=False)["input_ids"])

        # ---- calibration (skip=0 inside; the frames' own rendered prompts) ----
        self.calibrate([f["rendered"] for f in frames_meta.values()])
        calib_norm = float(self.engine._require_norms().get(self.layer, 0.0))
        # α = strength × calibration_norm must be the SAME scaling the source
        # run's generation-time steering used; cross-check against its header.
        main_meta = source_meta[0]
        src_calib = main_meta.get("calibration_norm")
        if (src_calib and main_meta.get("steer_layer") == self.layer
                and abs(calib_norm - float(src_calib)) > 0.01 * abs(float(src_calib))):
            import warnings
            warnings.warn(
                f"re-derived calibration norm {calib_norm:.3f} differs from the source "
                f"run's {float(src_calib):.3f} by >1% at layer {self.layer} — direct-add "
                "α and the steered re-encode would not match the generation-time "
                "scaling (wrong model/frames pairing?)", stacklevel=2)

        # ---- frozen coordinates ----
        dirs = directions if directions is not None else frozen_directions(self.capture_layers)
        dirs = {int(L): {n: _unit(v) for n, v in d.items()} for L, d in dirs.items()}
        missing = [L for L in self.capture_layers if L not in dirs]
        if missing:
            raise KeyError(f"directions missing capture layers {missing}")
        bad_names = {L: [n for n in REQUIRED_DIRECTIONS if n not in dirs[L]]
                     for L in self.capture_layers}
        bad_names = {L: m for L, m in bad_names.items() if m}
        if bad_names:
            raise KeyError("directions missing required confirmatory coordinates "
                           f"(prereg E3 z_a/z_x family): {bad_names}")

        # ---- injected directions per condition ----
        synth = self._synthetic_vectors(seed=seed)
        # __random__ is reproducible ONLY under the seed the source run used; a
        # mismatched seed would silently steer a DIFFERENT random vector while
        # still labeling rows "__random__" (a mislabeled control band).
        # __mean_all__ is seed-independent and is unaffected.
        src_seeds = {int(m["seed"]) for m in source_meta if m.get("seed") is not None}
        random_seed_ok = (not src_seeds) or src_seeds == {int(seed)}
        conditions = sorted({r["condition"] for r in kept})
        v_layer: dict[str, dict[int, np.ndarray]] = {}   # emotion's own dir at each layer
        v_steer: dict[str, np.ndarray] = {}              # the vector actually injected
        skipped: list[str] = []
        for c in conditions:
            if c == BASELINE:
                continue
            if c == qualia.RANDOM_DIRECTION and not random_seed_ok:
                skipped.append(c)
                continue
            if c in synth:
                v_steer[c] = _unit(synth[c])
                if c == qualia.MEAN_ALL_DIRECTION:
                    v_layer[c] = {L: _unit(self.bank.vectors[L].mean(axis=0))
                                  for L in self.capture_layers}
                else:
                    v_layer[c] = {L: _unit(synth[c]) for L in self.capture_layers}
                continue
            try:
                idx = self.bank.index_of(c)
            except (KeyError, ValueError):
                skipped.append(c)
                continue
            v_steer[c] = _unit(self.bank.vectors[self.layer, idx])
            v_layer[c] = {L: _unit(self.bank.vectors[L, idx]) for L in self.capture_layers}

        # ---- provenance geometry: Gram matrix + cos(v_e, coordinate) table ----
        names = sorted(dirs[self.capture_layers[0]])
        gram = {str(L): {a: {b: round(float(dirs[L][a] @ dirs[L][b]), 5) for b in names}
                         for a in names} for L in self.capture_layers}
        cos_v_e = {c: {str(L): {n: round(float(v_layer[c][L] @ dirs[L][n]), 5)
                                for n in names}
                       for L in self.capture_layers} for c in sorted(v_layer)}

        rows: list[dict] = []

        def rec_base(r: dict, arm: str, encode_frame: str, span: str) -> dict:
            return {"record_id": r["record_id"], "arm": arm,
                    "condition": r["condition"], "strength": float(r["strength"]),
                    "frame": r["frame"], "encode_frame": encode_frame,
                    "sample": r.get("sample"), "span": span,
                    "descriptive_only": bool(r.get("descriptive_only", False))}

        # ---- arms 1 + 3: unsteered re-encode (own text / swapped context) ----
        for arm, swap in ((ARM_REENC_UNSTEERED, False), (ARM_REENC_UNSTEERED_SWAP, True)):
            if arm not in arms:
                continue
            for r in kept:
                ef = SWAP_FRAMES.get(r["frame"]) if swap else r["frame"]
                if ef is None or ef not in frames_meta:
                    continue
                f = frames_meta[ef]
                enc = self._reencode(f["rendered"], r["text"], n_prompt=f["prompt_tokens"])
                base = rec_base(r, arm, ef, "response")
                base["response_tokens"] = enc["response_tokens"]
                self._emit(rows, base, dirs, enc["pooled"], v_layer.get(r["condition"]),
                           v_steer.get(r["condition"]), enc["washout_pooled"],
                           enc["washout_tokens"])

        # ---- arms 2 + 4: steered re-encode (one steer context per cell) ----
        # Hook order is load-bearing: the steer context is entered BEFORE
        # _reencode registers the capture hooks, so at the steer layer the
        # capture sees the POST-injection residual (torch runs forward hooks in
        # registration order, threading each hook's returned output into the
        # next). This is exactly what the prereg's injection-artifact handling
        # assumes ("at layers >= 42 the captured state literally contains
        # α·v_emotion"). Do not reorder.
        steered_cells: dict[tuple, list[dict]] = {}
        for r in kept:
            if r["strength"] > 0 and r["condition"] in v_steer:
                steered_cells.setdefault((r["condition"], r["strength"]), []).append(r)
        for arm, swap in ((ARM_REENC_STEERED, False), (ARM_REENC_STEERED_SWAP, True)):
            if arm not in arms:
                continue
            for (cond, s), cell_records in sorted(steered_cells.items()):
                with self._steer(cond, float(s), synth):
                    for r in cell_records:
                        ef = SWAP_FRAMES.get(r["frame"]) if swap else r["frame"]
                        if ef is None or ef not in frames_meta:
                            continue
                        f = frames_meta[ef]
                        enc = self._reencode(f["rendered"], r["text"],
                                             n_prompt=f["prompt_tokens"])
                        base = rec_base(r, arm, ef, "response")
                        base["response_tokens"] = enc["response_tokens"]
                        self._emit(rows, base, dirs, enc["pooled"], v_layer.get(cond),
                                   v_steer.get(cond), enc["washout_pooled"],
                                   enc["washout_tokens"])

        # ---- arm 5: direct-add analytic null (prompt-only, numeric injection) ----
        if ARM_DIRECT_ADD in arms:
            prompt_acts = {fk: self._prompt_acts(frames_meta[fk]["rendered"])
                           for fk in sorted({r["frame"] for r in kept})}
            # strength-0 base points, one per frame
            for fk, acts in sorted(prompt_acts.items()):
                base = {"record_id": f"cell:{BASELINE}:0.0:{fk}", "arm": ARM_DIRECT_ADD,
                        "condition": BASELINE, "strength": 0.0, "frame": fk,
                        "encode_frame": fk, "sample": None, "span": "prompt_final",
                        "descriptive_only": False}
                self._emit(rows, base, dirs, acts, None, None)
            cells = sorted({(r["condition"], r["strength"], r["frame"]) for r in kept
                            if r["strength"] > 0 and r["condition"] in v_steer})
            for cond, s, fk in cells:
                alpha = float(s) * calib_norm
                add = alpha * v_steer[cond]
                pooled = {L: (prompt_acts[fk][L] + add if L >= self.layer
                              else prompt_acts[fk][L])
                          for L in self.capture_layers}
                base = {"record_id": f"cell:{cond}:{_snap(s)}:{fk}", "arm": ARM_DIRECT_ADD,
                        "condition": cond, "strength": float(s), "frame": fk,
                        "encode_frame": fk, "sample": None, "span": "prompt_final",
                        "descriptive_only": _snap(s) in {_snap(x) for x in DESCRIPTIVE_STRENGTHS}}
                self._emit(rows, base, dirs, pooled, v_layer.get(cond), v_steer.get(cond))

        # ---- arm 6: prompt-only steered forward (propagation, no generation) ----
        if ARM_PROMPT_STEERED in arms:
            frames_present = sorted({r["frame"] for r in kept})
            for (cond, s) in sorted(steered_cells):
                with self._steer(cond, float(s), synth):
                    for fk in frames_present:
                        acts = self._prompt_acts(frames_meta[fk]["rendered"])
                        base = {"record_id": f"cell:{cond}:{_snap(s)}:{fk}",
                                "arm": ARM_PROMPT_STEERED, "condition": cond,
                                "strength": float(s), "frame": fk, "encode_frame": fk,
                                "sample": None, "span": "prompt_final",
                                "descriptive_only": _snap(s) in {_snap(x) for x in DESCRIPTIVE_STRENGTHS}}
                        self._emit(rows, base, dirs, acts, v_layer.get(cond),
                                   v_steer.get(cond))

        # ---- arm 7: persona-induction positive control (generates!) ----
        role_prompts: dict[str, str] = {}
        if ARM_PERSONA_CONTROL in arms:
            for role in control_roles:
                sys_prompt = load_role_instruction(role, role_root)
                role_prompts[role] = sys_prompt
                for fk, f in sorted(frames_meta.items()):
                    rendered = self._render_conv([
                        {"role": "system", "content": sys_prompt},
                        {"role": "user", "content": f["text"]},
                    ])
                    texts = self._gen_samples(rendered, n_control, max_new_tokens,
                                              temperature, gen_batch)
                    for i, text in enumerate(texts):
                        enc = self._reencode(rendered, text)   # unsteered, own tokenization
                        base = {"record_id": f"role:{role}:{fk}:{i}",
                                "arm": ARM_PERSONA_CONTROL, "condition": f"role:{role}",
                                "strength": 0.0, "frame": fk, "encode_frame": fk,
                                "sample": i, "span": "response",
                                "descriptive_only": False,
                                "response_tokens": enc["response_tokens"]}
                        self._emit(rows, base, dirs, enc["pooled"], None, None,
                                   enc["washout_pooled"], enc["washout_tokens"])

        # ---- payload ----
        cond_meta = []
        for c in conditions:
            spec = qualia.spec_for(c)
            cond_meta.append({"name": c, "steerable": c in v_steer,
                              "valence": spec.valence if spec else None,
                              "group": spec.group if spec else None})
        return {
            "model_id": self.config.model_id,
            "suite_key": os.environ.get("SUITE_KEY"),
            "experiment": "persona_displacement",
            "steer_layer": self.layer,
            "capture_layers": [int(L) for L in self.capture_layers],
            "layer_roles": {str(L): LAYER_ROLES.get(L, "exploratory")
                            for L in self.capture_layers},
            "num_layers": self.model.num_layers,
            "enable_thinking": self.config.enable_thinking,
            "calibration_norm": calib_norm,
            "strengths_confirmatory": sorted(_snap(s) for s in confirmatory),
            "strengths_descriptive": sorted(_snap(s) for s in DESCRIPTIVE_STRENGTHS),
            "arms": arms,
            "seed": int(seed),
            "n_control": int(n_control),
            "control_roles": role_prompts,
            "sources": source_meta,
            "n_source_records": len(all_records),
            "n_kept_records": len(kept),
            "frames": [{"key": k, **{kk: f.get(kk) for kk in
                                     ("label", "kind", "text", "rendered", "prompt_tokens")}}
                       for k, f in sorted(frames_meta.items())],
            "conditions": cond_meta,
            "skipped_conditions": sorted(skipped),
            "direction_names": names,
            "direction_gram": gram,
            "cos_v_e": cos_v_e,
            "records": rows,
        }


