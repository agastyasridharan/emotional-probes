"""
E3 persona-coordinate displacement re-encode driver — the GPU driver.

Spec: docs/research/portfolio.md §E3 (binding) + docs/prereg/E3.md. Effect-side
keystone: WHICH persona coordinates move under emotion steering, and is the
movement SELF/OTHER frame-dependent? Diagnostic, not causal — it aims E5.

Data reuse is mandatory: no new emotion-steered generation, no new judging. The
driver re-encodes the EXISTING scored free-form generations
(``suite/<key>/analysis/qualia_freeform_scored.json``; records carry
condition/strength/frame/text) with teacher-forced forward passes, captures the
residual stream at layers {32, 42, 50} simultaneously (in-driver multi-layer hook
helper — :class:`ProbedModel` stays untouched), mean-pools over the response-token
span (from the recorded ``prompt_tokens``), and stores raw projections onto the
frozen E1 coordinates. Token-resolved projections at first-person-pronoun /
experience-claim positions are recorded as the washout guard. Nothing is
aggregated on GPU; all confirmatory inference (T1-T6, exact 252-enumeration via
``scripts/exact_perm.py``) is CPU-side and lives in a separate stats script.

Arms (portfolio §E3 fix arms; the 2×2 re-encode is arms 1-4):

* ``reencode_unsteered``       — each record's own text, unsteered forward
                                 (text-driven component; includes the per-frame
                                 strength-0 baselines).
* ``reencode_steered``         — own text with the record's (condition, strength)
                                 steer context active during the forward pass
                                 (state + text).
* ``reencode_unsteered_swap``  — text swapped across SELF/OTHER contexts,
                                 unsteered (text-driven displacement follows the
                                 text).
* ``reencode_steered_swap``    — swapped text, steer context active (state-driven
                                 displacement follows the steer context).
* ``direct_add``               — analytic null: unsteered prompt-only forward,
                                 then α·v_e added NUMERICALLY at layers ≥ the
                                 injection layer (α = strength × calibration
                                 norm) — the pure linear-propagation prediction
                                 to subtract. Includes the strength-0 prompt
                                 cells as the base points.
* ``prompt_steered``           — prompt-only forward under the steer context
                                 (actual propagation without generation).
* ``persona_control``          — positive control: system-prompt role inductions
                                 (empath / poet / accountant), n generations per
                                 role × frame through the identical capture
                                 pipeline — sets the axis-displacement scale for
                                 a "detectable persona shift".

Projections per (record, arm, layer): assistant axis a[L], human axis h[L],
h_purged[L], experiencer axis z_x[L] (prereg E3: unit(mean frozen
experiencer_high roles − frozen experiencer_low roles), orthogonalized against
a — built here from the frozen role subsets + atlas, never re-derived),
valence axis[L], PC1-8 role-space coordinates, plus the injected
emotion's own direction two ways — ``v_injected`` (the emotion's layer-local
direction v_e[L]) and ``v_injected_steer`` (the vector actually added at the
injection layer, the one linear propagation carries to L50; this is what the
injection-artifact residualization needs). The per-layer coordinate Gram matrix
and the cos(v_e, coordinate) table are published in the provenance header
(prereg: "publish the cos table up front"; collinearity gate reads the Gram).

Strengths: confirmatory {0, 0.06, 0.10}; s=0.15 records are kept but flagged
``descriptive_only`` (self_attribution saturates the scale there — portfolio
REJECTED list); other shipped strengths (0.03) are dropped. The
``__random__``/``__mean_all__`` top-up mandated by §E3 is generated by re-running
the *freeform* driver (that is where generation lives) and feeding the scored
result here via ``--extra-scored``.

INTEGRATION (E1 workstream, ``emotion_probes.persona.geometry`` — not owned by
this file; :func:`frozen_directions` is the single adapter point, verified
against the landed API):

    geometry.build_directions(bank, ax, subsets, layer, seed=0)
        -> {"a", "h", "h_purged", "valence", "v_<emotion>": unit (5120,)}
    geometry.pca_atlas(ax, layer, center="default", n_pcs=8)
        -> {..., "components": (n_pcs, 5120)}

If geometry's API moves, only :func:`frozen_directions` changes; alternatively
E1 can export the frozen coordinates to an ``.npz`` (keys ``"<name>@L<layer>"``)
consumed via ``--directions``. Tests inject directions straight into
:meth:`PersonaDisplacementExperiment.run`.

Requirements: a real chat model on a GPU (torch imported lazily; the module
imports and unit-tests on a laptop).

    python -m emotion_probes.alignment.persona_displacement --suite qwen3-32b \
        --layers 32 42 50
    python -m emotion_probes.alignment.persona_displacement --quick   # smoke
"""

