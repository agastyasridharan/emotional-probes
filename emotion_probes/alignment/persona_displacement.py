def load_scored(paths: Sequence[tuple[str, Path]]) -> tuple[list[dict], dict, list[dict]]:
    """Load one or more ``qualia_freeform_scored.json`` payloads.

    Returns ``(records, frames_meta, source_meta)``. Each record keeps only the
    fields E3 needs plus a stable ``record_id`` = ``"<tag>#<index>"`` (index into
    that file's ``records`` list — downstream stats join judge scores back through
    it). ``frames_meta`` maps frame key -> the frame dict (first file wins);
    ``rendered``/``prompt_tokens`` may be absent in old payloads (caller fills).
    ``source_meta`` carries each source run's provenance (seed, calibration norm,
    steer layer, model id) — run() uses it to guard the ``__random__``
    reconstruction and the α-scaling consistency."""
    records: list[dict] = []
    frames_meta: dict[str, dict] = {}
    source_meta: list[dict] = []
    for tag, p in paths:
        payload = json.loads(Path(p).read_text())
        source_meta.append({
            "tag": tag, "path": str(p),
            "seed": payload.get("seed"),
            "calibration_norm": payload.get("calibration_norm"),
            "steer_layer": payload.get("layer"),
            "model_id": payload.get("model_id"),
        })
        for f in payload.get("frames", []):
            frames_meta.setdefault(f["key"], dict(f))
        for i, r in enumerate(payload.get("records", [])):
            records.append({
                "record_id": f"{tag}#{i}",
                "condition": r["condition"],
                "strength": _snap(r.get("strength", 0.0)),
                "frame": r["frame"],
                "sample": r.get("sample"),
                "text": r.get("text", ""),
            })
    return records, frames_meta, source_meta


def select_records(records: Sequence[dict], confirmatory: Sequence[float],
                   descriptive: Sequence[float] = DESCRIPTIVE_STRENGTHS,
                   emotions: Sequence[str] | None = None,
                   max_per_cell: int | None = None) -> list[dict]:
    """Filter to the pre-registered strengths and (optionally) a condition subset.

    A record is kept iff its snapped strength is confirmatory or descriptive;
    descriptive records gain ``descriptive_only=True``. ``__baseline__`` records
    are always kept, regardless of the strength filter (per-frame strength-0
    baselines are load-bearing — prereg E3). With ``max_per_cell``, keeps the
    first k records per (condition, strength, frame) cell (the --quick path)."""
    conf = {_snap(s) for s in confirmatory}
    desc = {_snap(s) for s in descriptive} - conf
    keep_names = set(emotions) if emotions is not None else None
    out: list[dict] = []
    cell_counts: dict[tuple, int] = {}
    for r in records:
        s = _snap(r["strength"])
        if r["condition"] != BASELINE and s not in conf and s not in desc:
            continue
        if keep_names is not None and r["condition"] != BASELINE \
                and r["condition"] not in keep_names:
            continue
        cell = (r["condition"], s, r["frame"])
        cell_counts[cell] = cell_counts.get(cell, 0) + 1
        if max_per_cell is not None and cell_counts[cell] > max_per_cell:
            continue
        out.append({**r, "strength": s, "descriptive_only": s in desc})
    return out


